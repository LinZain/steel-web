public class SteelNumberVO {
    private String steelNumber;
    private String stdNumber;
    private String steelType;
    private String similarMaterial;
    private String theSameMaterial;

    public String getSteelNumber() {
        return steelNumber;
    }

    public void setSteelNumber(String steelNumber) {
        this.steelNumber = steelNumber;
    }

    public String getStdNumber() {
        return stdNumber;
    }

    public void setStdNumber(String stdNumber) {
        this.stdNumber = stdNumber;
    }

    public String getSteelType() {
        return steelType;
    }

    public void setSteelType(String steelType) {
        this.steelType = steelType;
    }

    public String getSimilarMaterial() {
        return similarMaterial;
    }

    public void setSimilarMaterial(String similarMaterial) {
        this.similarMaterial = similarMaterial;
    }

    public String getTheSameMaterial() {
        return theSameMaterial;
    }

    public void setTheSameMaterial(String theSameMaterial) {
        this.theSameMaterial = theSameMaterial;
    }
}
